-- VISTAS NECESARIAS PARA LOS GRÁFICOS

-- Vista 1: Producto más vendido por sucursal
CREATE VIEW Vista_MasVendidoPorSucursal AS
SELECT 
    s.Nombre_Sucursal,
    p.Nombre_Producto,
    SUM(dc.Cantidad) as Total_Vendido
FROM Sucursal s
JOIN Compra c ON s.Id_Sucursal = c.sucursal_Id_Sucursal
JOIN Detalle_Compra dc ON c.Id_Compra = dc.compra_Id_Compra
JOIN Producto p ON dc.producto_Id_Producto = p.Id_Producto
GROUP BY s.Id_Sucursal, s.Nombre_Sucursal, p.Id_Producto, p.Nombre_Producto
HAVING SUM(dc.Cantidad) = (
    SELECT MAX(total_vendido)
    FROM (
        SELECT SUM(dc2.Cantidad) as total_vendido
        FROM Detalle_Compra dc2
        JOIN Compra c2 ON dc2.compra_Id_Compra = c2.Id_Compra
        JOIN Producto p2 ON dc2.producto_Id_Producto = p2.Id_Producto
        WHERE c2.sucursal_Id_Sucursal = s.Id_Sucursal
        GROUP BY p2.Id_Producto
    ) subquery
);

-- Vista 2 Compras mayores a $40
CREATE VIEW Vista_Compras_MayoresCuarenta AS
SELECT 
    c.Id_Compra,
    cl.Nombre_Cliente,
    cl.Appaterno_Cliente,
    SUM(dc.Cantidad * p.Precio) as Total
FROM Compra c
JOIN Cliente cl ON c.cliente_Id_Cliente = cl.Id_Cliente
JOIN Detalle_Compra dc ON c.Id_Compra = dc.compra_Id_Compra
JOIN Producto p ON dc.producto_Id_Producto = p.Id_Producto
GROUP BY c.Id_Compra, cl.Id_Cliente, cl.Nombre_Cliente, cl.Appaterno_Cliente
HAVING SUM(dc.Cantidad * p.Precio) > 400 ORDER BY Total DESC;

-- VISTAS ADICIONALES PARA MÁS GRÁFICOS

-- Vista 3: Ventas por categoría de producto
CREATE VIEW Vista_VentasPorCategoria AS
SELECT 
    cp.Nombre_Categoria,
    COUNT(dc.Id_Detalle) as Cantidad_Ventas,
    SUM(dc.Cantidad * p.Precio) as Total_Ventas
FROM Categoria_Producto cp
JOIN Producto p ON cp.Id_Categoria = p.categoria_producto_Id_Categoria
JOIN Detalle_Compra dc ON p.Id_Producto = dc.producto_Id_Producto
GROUP BY cp.Id_Categoria, cp.Nombre_Categoria
ORDER BY Total_Ventas DESC;

-- Vista 4: Ventas por método de pago
CREATE VIEW Vista_VentasPorMetodoPago AS
SELECT 
    p.Metodo_Pago,
    COUNT(c.Id_Compra) as Cantidad_Compras,
    SUM(dc.Cantidad * pr.Precio) as Total_Recaudado
FROM Pago p
JOIN Compra c ON p.Id_Pago = c.pago_Id_Pago
JOIN Detalle_Compra dc ON c.Id_Compra = dc.compra_Id_Compra
JOIN Producto pr ON dc.producto_Id_Producto = pr.Id_Producto
GROUP BY p.Id_Pago, p.Metodo_Pago
ORDER BY Total_Recaudado DESC;

-- FUNCIONES

-- Función para calcular el total de ventas de una sucursal
CREATE OR REPLACE FUNCTION CalcularTotalVentasSucursal(sucursal_id INT)
RETURNS NUMERIC AS $$
DECLARE
    total NUMERIC := 0;
BEGIN
    SELECT COALESCE(SUM(dc.Cantidad * p.Precio), 0) INTO total
    FROM Compra c
    JOIN Detalle_Compra dc ON c.Id_Compra = dc.compra_Id_Compra
    JOIN Producto p ON dc.producto_Id_Producto = p.Id_Producto
    WHERE c.sucursal_Id_Sucursal = sucursal_id;
    
    RETURN total;
END;
$$ LANGUAGE plpgsql;

-- Función para obtener productos favoritos de un cliente
CREATE OR REPLACE FUNCTION ObtenerProductosFavoritosCliente(cliente_id INT)
RETURNS TABLE (
    nombre_producto VARCHAR(50),
    valoracion INT,
    comentario VARCHAR(200)
) AS $$
BEGIN
    RETURN QUERY
    SELECT p.Nombre_Producto, pc.Valoracion, pc.Comentario
    FROM Preferencia_Cliente pc
    JOIN Producto p ON pc.Id_Producto_Favorito = p.Id_Producto
    WHERE pc.Id_Cliente = cliente_id
    ORDER BY pc.Valoracion DESC;
END;
$$ LANGUAGE plpgsql;

-- TRIGGERS

-- Trigger para actualizar automáticamente la fecha de compra
CREATE OR REPLACE FUNCTION actualizar_fecha_compra()
RETURNS TRIGGER AS $$
BEGIN
    NEW.Fecha_Compra = CURRENT_DATE;
    NEW.Hora_Compra = CURRENT_TIME;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_actualizar_fecha_compra
    BEFORE INSERT ON Compra
    FOR EACH ROW
    EXECUTE FUNCTION actualizar_fecha_compra();

-- Trigger para validar que la cantidad sea positiva
CREATE OR REPLACE FUNCTION validar_cantidad_positiva()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.Cantidad <= 0 THEN
        RAISE EXCEPTION 'La cantidad debe ser mayor a 0';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_validar_cantidad
    BEFORE INSERT OR UPDATE ON Detalle_Compra
    FOR EACH ROW
    EXECUTE FUNCTION validar_cantidad_positiva();

