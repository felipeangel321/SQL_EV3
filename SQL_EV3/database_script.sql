-- Procedimiento para registrar una nueva compra completa
CREATE OR REPLACE PROCEDURE RegistrarCompra(
    p_cliente_id INT,
    p_sucursal_id INT,
    p_pago_id INT,
    p_productos JSON
)
LANGUAGE plpgsql
AS $$
DECLARE
    nueva_compra_id INT;
    producto_record RECORD;
BEGIN
    -- Insertar la compra
    INSERT INTO Compra (cliente_Id_Cliente, sucursal_Id_Sucursal, pago_Id_Pago)
    VALUES (p_cliente_id, p_sucursal_id, p_pago_id)
    RETURNING Id_Compra INTO nueva_compra_id;
    
    -- Insertar los detalles de la compra
    FOR producto_record IN 
        SELECT * FROM json_array_elements(p_productos) AS p(producto_id INT, cantidad INT)
    LOOP
        INSERT INTO Detalle_Compra (Cantidad, producto_Id_Producto, compra_Id_Compra)
        VALUES (producto_record.cantidad, producto_record.producto_id, nueva_compra_id);
    END LOOP;
    
    COMMIT;
END;
$$;

-- Procedimiento para generar reporte de ventas por fecha
CREATE OR REPLACE PROCEDURE GenerarReporteVentas(
    p_fecha_inicio DATE,
    p_fecha_fin DATE
)
LANGUAGE plpgsql
AS $$
DECLARE
    total_ventas NUMERIC;
    total_compras INT;
BEGIN
    SELECT 
        COALESCE(SUM(dc.Cantidad * p.Precio),0),
        COUNT(DISTINCT c.Id_Compra)
    INTO total_ventas, total_compras
    FROM Compra c
    JOIN Detalle_Compra dc ON c.Id_Compra = dc.compra_Id_Compra
    JOIN Producto p ON dc.producto_Id_Producto = p.Id_Producto
    WHERE c.Fecha_Compra BETWEEN p_fecha_inicio AND p_fecha_fin;
    
    RAISE NOTICE 'Reporte de ventas del % al %', p_fecha_inicio, p_fecha_fin;
    RAISE NOTICE 'Total de ventas: $%', total_ventas;
    RAISE NOTICE 'Total de compras: %', total_compras;
    RAISE NOTICE 'Promedio por compra: $%', CASE WHEN total_compras > 0 THEN total_ventas / total_compras ELSE 0 END;
END;
$$; 