
-- Tabla COMUNA
CREATE TABLE Comuna (
    Id_Comuna SERIAL PRIMARY KEY,
    Nombre_comuna VARCHAR(50) NOT NULL
);

-- Tabla CLIENTE
CREATE TABLE Cliente (
    Id_Cliente SERIAL PRIMARY KEY,
    Nombre_Cliente VARCHAR(30) NOT NULL,
    Appaterno_Cliente VARCHAR(30) NOT NULL,
    Apmaterno_Cliente VARCHAR(30),
    Fecha_Nac DATE NOT NULL,
    Genero VARCHAR(25) NOT NULL,
    Direccion_Cliente VARCHAR(75) NOT NULL,
    comuna_Id_Comuna INT NOT NULL,
    CONSTRAINT fk_comuna FOREIGN KEY (comuna_Id_Comuna) REFERENCES Comuna(Id_Comuna)
);

-- Tabla SUCURSAL
CREATE TABLE Sucursal (
    Id_Sucursal SERIAL PRIMARY KEY,
    Nombre_Sucursal VARCHAR(50) NOT NULL,
    Direccion_Suc VARCHAR(50) NOT NULL,
    Hra_Apertura TIME NOT NULL,
    Hra_Cerrado TIME NOT NULL
);

-- Tabla PAGO 
CREATE TABLE Pago (
    Id_Pago SERIAL PRIMARY KEY,
    Metodo_Pago VARCHAR(30) NOT NULL
);

-- Tabla COMPRA
CREATE TABLE Compra (
    Id_Compra SERIAL PRIMARY KEY,
    Fecha_Compra DATE NOT NULL,
    Hora_Compra TIME NOT NULL,
    cliente_Id_Cliente INT NOT NULL,
    sucursal_Id_Sucursal INT NOT NULL,
    pago_Id_Pago INT NOT NULL,
    CONSTRAINT fk_cliente FOREIGN KEY (cliente_Id_Cliente) REFERENCES Cliente(Id_Cliente),
    CONSTRAINT fk_sucursal FOREIGN KEY (sucursal_Id_Sucursal) REFERENCES Sucursal(Id_Sucursal),
    CONSTRAINT fk_pago FOREIGN KEY (pago_Id_Pago) REFERENCES Pago(Id_Pago)
);

-- Tabla CATEGORIA_PRODUCTO
CREATE TABLE Categoria_Producto (
    Id_Categoria SERIAL PRIMARY KEY,
    Nombre_Categoria VARCHAR(50) NOT NULL
);

-- Tabla PRODUCTO
CREATE TABLE Producto (
    Id_Producto SERIAL PRIMARY KEY,
    Nombre_Producto VARCHAR(50) NOT NULL,
    Precio NUMERIC(10, 2) NOT NULL,
    Descripcion VARCHAR(150) NOT NULL,
    categoria_producto_Id_Categoria INT NOT NULL,
    CONSTRAINT fk_categoria FOREIGN KEY (categoria_producto_Id_Categoria) REFERENCES Categoria_Producto(Id_Categoria)
);

-- Tabla DETALLE_COMPRA
CREATE TABLE Detalle_Compra (
    Id_Detalle SERIAL PRIMARY KEY,
    Cantidad INT NOT NULL,
    producto_Id_Producto INT NOT NULL,
    compra_Id_Compra INT NOT NULL,
    CONSTRAINT fk_producto FOREIGN KEY (producto_Id_Producto) REFERENCES Producto(Id_Producto),
    CONSTRAINT fk_compra FOREIGN KEY (compra_Id_Compra) REFERENCES Compra(Id_Compra)
);

-- Tabla PREFERENCIA_CLIENTE
CREATE TABLE Preferencia_Cliente (
    Id_Preferencia SERIAL PRIMARY KEY,
    Id_Cliente INT NOT NULL,
    Id_Producto_Favorito INT NOT NULL,
    Frecuencia_Compra VARCHAR(50),
    Comentario VARCHAR(200),
    Valoracion INT CHECK (Valoracion BETWEEN 1 AND 7),
    CONSTRAINT fk_cliente_pref FOREIGN KEY (Id_Cliente) REFERENCES Cliente(Id_Cliente),
    CONSTRAINT fk_producto_fav FOREIGN KEY (Id_Producto_Favorito) REFERENCES Producto(Id_Producto)
);

